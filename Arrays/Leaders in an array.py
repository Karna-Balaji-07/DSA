# leaders in an array
# An element of array is leader if it is greater than or equal to all the elements to its right side. The rightmost element is always a leader.

def leaders(arr):
    a = -1
    L = []
    for i in range(len(arr)-1,-1,-1):
        if arr[i] > a:
            a = arr[i]
            L.append(arr[i])

    return L[::-1]

def Leader(A,N):
    ans = []
    maxx = A[N - 1]
    for i in range(N - 1, -1, -1):
        if A[i] >= maxx:
            maxx = A[i]
            ans.append(maxx)

    ans.reverse()

    return ans

